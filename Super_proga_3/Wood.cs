namespace Super_Proga_3
{
    class Wood
    {
        public Wood()
        {           
        }
        public Wood(string Name, double Price)
        {
            this.Name = Name;
            this.Price = Price;            
        }

        public double Price;
        public string Name;
        public double TotalSquare;
        public double TotalAmount;
        public double Square;
        public double Amount;
        public int length;
        public int width;
        public int quantity;
        public double thickness;
        public double volume;
        public double TotalVolume;
        public double id;
    }


   
}
